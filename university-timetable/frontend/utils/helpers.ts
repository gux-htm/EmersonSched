import { clsx, type ClassValue } from 'clsx';
import { twMerge } from 'tailwind-merge';
import { format, parseISO, isValid } from 'date-fns';

// Utility function to merge Tailwind classes
export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

// Date formatting utilities
export const formatDate = (date: string | Date, formatStr: string = 'MMM dd, yyyy'): string => {
  try {
    const dateObj = typeof date === 'string' ? parseISO(date) : date;
    return isValid(dateObj) ? format(dateObj, formatStr) : 'Invalid Date';
  } catch (error) {
    return 'Invalid Date';
  }
};

export const formatTime = (time: string, format12Hour: boolean = true): string => {
  try {
    // Handle time strings like "14:30:00" or "14:30"
    const [hours, minutes] = time.split(':').map(Number);
    
    if (format12Hour) {
      const period = hours >= 12 ? 'PM' : 'AM';
      const displayHours = hours === 0 ? 12 : hours > 12 ? hours - 12 : hours;
      return `${displayHours}:${minutes.toString().padStart(2, '0')} ${period}`;
    } else {
      return `${hours.toString().padStart(2, '0')}:${minutes.toString().padStart(2, '0')}`;
    }
  } catch (error) {
    return time;
  }
};

export const formatDateTime = (dateTime: string, formatStr: string = 'MMM dd, yyyy HH:mm'): string => {
  try {
    const dateObj = parseISO(dateTime);
    return isValid(dateObj) ? format(dateObj, formatStr) : 'Invalid DateTime';
  } catch (error) {
    return 'Invalid DateTime';
  }
};

// Credit hours parsing
export const parseCreditHours = (creditHours: string) => {
  const parts = creditHours.split('+');
  const theory = parseInt(parts[0]) || 0;
  const lab = parseInt(parts[1]) || 0;
  
  return { theory, lab, total: theory + lab };
};

